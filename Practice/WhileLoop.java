package Practice;
public class WhileLoop
{
	public static void main(String args[])
	{
		byte i=1;
		while(i<=10)
		{
			System.out.println("Welcome "+i);
			i++;
		}
		System.out.println("\nMeeting Hall is full");
	}
}
