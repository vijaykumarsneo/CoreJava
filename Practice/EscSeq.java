package Practice;
public class EscSeq
{
	public static void main(String args[])
	{
		System.out.println("The  \bString  \bclass");	//insert a backspace
		System.out.println("Hello!\nHow are you?");		//insert a new line
		System.out.println("Hi,\tThis is Vijay");		//insert a horizontal tab
		System.out.println("Nice 2 meet you!\\Vijay");	//insert a backslash
		System.out.println("The \"String\" class");		//insert a double quote
	}
}